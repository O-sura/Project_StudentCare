<h3><strong>Student Community</strong></h3>
<div class="content">
  <p>We encourage all students to participate in our student community and share their experiences and ideas with other students. However, we expect our users to abide by the following rules:</p>

  <ul>
    <li>Respect other users and their opinions.</li>
    <li>Do not post any offensive or harmful content.</li>
    <li>Do not spam or promote any products or services.</li>
    <li>Do not share personal information without consent.</li>
    <li>Anything which violates the policy or community guidelines will be subjected as offence and will be removed immediately without any prior notices.</li>
  </ul>
  
</div>

<h3><strong>Counseling Services</strong></h3>
<div class="content">
  <p>We provide free counseling sessions to our users based on their needs. However, we have the following rules in place:</p>
    <ul>
      <li>Appointments must be made in advance.</li>
      <li>Do not cancel appointments without a valid reason.</li>
      <li>Respect the privacy and confidentiality of our counselors.</li>
      <li>Do not share any offensive or harmful content during sessions.</li>
    </ul>
</div>

